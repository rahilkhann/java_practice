public class Switch {
    public static void main(String[] args) {

        int condition = 1;
        switch(condition){
            case 1:
                System.out.print("Case 1");
                break;
            case 2:
                System.out.print("Case 2");
                break;
            case 3:
                System.out.print("Case 3");
                break;
            default:
                System.out.print("Case 4");
                break;
            
        }
        
    }
}
